package ex_07_Java_Forloop;

public class Lab019_table9wfor {
    public static void main(String[] args) {
        int a=9;
        System.out.println("Table of "+a+":");
        for (int i=1;i<=10;i++)
        {
            //System.out.println(a"*"i=);
            System.out.println(a+"x"+i+"="+(a*i));
        }
    }


}
